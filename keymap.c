bool process_record_user(uint16_t keycode, keyrecord_t *record) {
  switch (keycode) {
    case QWERTY:
      if (record->event.pressed) {
        set_single_persistent_default_layer(_QWERTY);
      }
      return false;
      break;
    case FN:
      if (record->event.pressed) {
        layer_on(_FN);
        update_tri_layer(_FN, _MOD, _ADJUST);
      } else {
        layer_off(_FN);
        update_tri_layer(_FN, _MOD, _ADJUST);
      }
      return false;
      break;
    case MOD:
      if (record->event.pressed) {
        layer_on(_MOD);
        update_tri_layer(_FN, _MOD, _ADJUST);
      } else {
        layer_off(_MOD);
        update_tri_layer(_FN, _MOD, _ADJUST);
      }
      return false;
      break;
    case ADJUST:
      if (record->event.pressed) {
        layer_on(_ADJUST);
      } else {
        layer_off(_ADJUST);
      }
      return false;
      break;
  }
  return true;
}
